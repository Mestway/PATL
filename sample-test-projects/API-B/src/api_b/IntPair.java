package api_b;

public class IntPair {
	Integer first;
	Integer second;
	
	public IntPair(Integer l, Integer r) {
		first = l;
		second = r;
	}
	
	public Integer getFirst() {
		return first;
	}
	
	public Integer getSecond() {
		return second;
	}
	
	public void setFirst(Integer s) {
		first = s;
	}
	
	public void setSecond(Integer s) {
		second = s;
	}
}
