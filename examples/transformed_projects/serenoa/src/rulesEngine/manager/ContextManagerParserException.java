package rulesEngine.manager;


public class ContextManagerParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ContextManagerParserException() {
		super();
	}

	public ContextManagerParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContextManagerParserException(String message) {
		super(message);
	}

	public ContextManagerParserException(Throwable cause) {
		super(cause);
	}
	
	

}
